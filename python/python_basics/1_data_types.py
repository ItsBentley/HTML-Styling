# Data Types

# Int - Any number without a decimal point.
48793
-26543

# Float - Any number with a decimal point.
48.79
-26.543

# String - A series of characters surrounded by double or single quotes.
"This is a string."
"This is also a string."

# Boolean - True or False.
True
False

# List - A series of values separated by commas.
[1, 2, 3, 4, 5]

# Tuple - A series of values separated by commas.
(1, 2, 3, 4, 5)

# Dictionary - A series of key-value pairs separated by commas.
{"key1": "value1", "key2": "value2", "key3": "value3"}

# Set - A series of unique values separated by commas.
{1, 2, 3, 4, 5}
