# Conditional operators

""" Conditional operators in Python are used to create conditional expressions or statements that evaluate to either True or False. They are also known as comparison operators or relational operators. Conditional operators are used to compare values and make decisions based on the comparison results.

Here are the conditional operators in Python:

Equal to (==): It checks if two values are equal.
Not equal to (!=): It checks if two values are not equal.
Greater than (>): It checks if the left operand is greater than the right operand.
Less than (<): It checks if the left operand is less than the right operand.
Greater than or equal to (>=): It checks if the left operand is greater than or equal to the right operand.
Less than or equal to (<=): It checks if the left operand is less than or equal to the right operand.
These operators can be used in conditional statements, such as if statements, to control the flow of the program based on the comparison results """

x = "hello"
y = 3
print(x * y)  # You can multiply a string by a number

x = "hello"
y = "world"
print(x + y)  # You can add two strings, this is a concatenation

# ==,!=, <=, >=, <, > This are conditional operators

x = "hello"
y = "hello"
print(x == y)  # This will print true if both strings are equal
print(x != y)  # This will print true if both strings are not equal

x = 4
y = 9
print(x > y)  # This will print true if x is greater than y
print(x >= y)  # This will print true if x is greater than or equal to y
print(x < y)  # This will print true if x is less than y
print(x <= y)  # This will print true if x is less than or equal to y
